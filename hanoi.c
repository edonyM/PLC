// solve for Tower of Hanoi Problem
#include <stdlib.h>
#include <stdio.h>

void hanoi(int n, char A, char B, char C) {
    if (n == 1) {
        printf("%c --> %c", A, C);
    }
    else {
        hanoi(n-1, A, C, B);
        hanoi(1, A, B, C);
        hanoi(n-1, B, A, C);
    }
}

int main() {
    int n;
    char A = 'A';
    char B = 'B';
    char C = 'C';
    scanf("%i", &n);
    hanoi(n, A, B, C);
}
