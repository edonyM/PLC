#include <stdio.h>
#include <stdlib.h>
#include <string.h>

void test1() {
    char string[10];
    char *str="0123456789";
    strcpy(string, str);
    printf("%s\n", str);
    printf("%c\n", string[10]);
}

void test2(char *str) {
    char string[10];
    if (strlen(str) <= 10) strcpy(string, str);
    printf("%s\n", string);
}
int main() {
    test1();
    char *str = "0123456789";
    test2(str);
}
