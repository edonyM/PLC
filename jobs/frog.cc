#include <iostream>

using std::cout;
using std::cin;
using std::endl;

void frog(int n, int &ways) {
    if (n == 0) {ways++;return;}
    if (n == 1) {ways++;return;}
    frog(n-1, ways);
    frog(n-2, ways);
}

int main() {
    int n;
    cin >> n;
    int ways = 0;
    frog(n, ways);
    cout << ways << endl;
}
