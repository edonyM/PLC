#include <stdio.h>

#define LOWER 0
#define UPPER 300
#define STEP 20

float fahr2cel(float fahr) {
    return (5.0 / 9.0) * (fahr - 32);
}

int main() {
    float fahr;
    printf("Enter the fahr number:");
    scanf("%f", &fahr);
    printf("%.2f fahr equals %.2f celsius\n", fahr, fahr2cel(fahr));
}
